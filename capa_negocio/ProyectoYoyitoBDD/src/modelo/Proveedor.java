package modelo;

/**
 *
 * @author felip
 */
public class Proveedor {
    private String nom;
    private String razon;
    private String rut;
    private String fono;
    private String mail;

    public Proveedor(String nom, String razon, String rut, String fono, String mail) {
        this.nom = nom;
        this.razon = razon;
        this.rut = rut;
        this.fono = fono;
        this.mail = mail;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getRazon() {
        return razon;
    }

    public void setRazon(String razon) {
        this.razon = razon;
    }

    public String getRut() {
        return rut;
    }

    public void setRut(String rut) {
        this.rut = rut;
    }

    public String getFono() {
        return fono;
    }

    public void setFono(String fono) {
        this.fono = fono;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }
    
    
    
}
